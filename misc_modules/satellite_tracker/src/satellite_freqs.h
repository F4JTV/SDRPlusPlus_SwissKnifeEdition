// Find the first/primary downlink for a NORAD id.
inline bool lookupDownlink(int norad, double& hz, std::string& label) {
    for (const auto& e : SAT_FREQS) {
        if (e.norad == norad) { hz = e.downlinkHz; label = e.label; return true; }
    }
    return false;
}

// Append every known downlink for a NORAD id. Returns the count appended.
template <typename Vec>
inline int collectDownlinks(int norad, Vec& out) {
    int n = 0;
    for (const auto& e : SAT_FREQS) {
        if (e.norad == norad) { out.push_back(e); n++; }
    }
    return n;
}

